package com.example.kelimegezmece.views;

public class Coordinat {
    private float x;
    private float y;

    public Coordinat() {
    }

    public Coordinat(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}